#include <iostream>
using namespace std;

int main() {
    int x = 5;
    cout << x << endl;

    x += 3; // x = x + 3      + : addition
    cout << x << endl;

    x -= 3; // x = x - 3      - : subtraction
    cout << x << endl;

    x *= 3; // x = x * 3      * : multiplication
    cout << x << endl;

    x /= 3; // x = x / 3      / : division
    cout << x << endl;

    x %= 3; // x = x % 3      % : modulus or remainder
    cout << x << endl;

    return 0;
}
